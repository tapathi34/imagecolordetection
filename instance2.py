# Function to calculate similarity between two instances
def calculate_similarity(instance1, instance2):
    # In this example, we use Euclidean distance as the similarity measure
    return sum((x1 - x2) ** 2 for x1, x2 in zip(instance1, instance2)) ** 0.5

# Function to perform Case-Based Reasoning (CBR) prediction
def cbr_prediction(new_instance, cases):
    similarities = []
    for case in cases:
        similarity = calculate_similarity(case['features'], new_instance)
        similarities.append((similarity, case['label']))
    similarities.sort()  # Sort the similarities in ascending order
    return max(set(label for _, label in similarities[:3]), key=similarities[:3].count)

